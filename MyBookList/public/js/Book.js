function Book(_id, title, genre, author, series, series_number, book_cover, description){
  this._id = _id;
  this.title = title;
  this.genre = genre;
  this.author = author;
  this.series = series;
  this.series_number = series_number;
  this.book_cover = book_cover;
  this.description = description;
}
